package test;

public class Decrement_Increment {

	public static void main(String[] args) {

		int a=1;
		int b=a++;
		System.out.println(a);
		System.out.println(b);
		
		int a1=1;
		int b1=++a1;
		System.out.println(a1);
		System.out.println(b1);
		
		int a2=1;
		int b2=a2--;
		System.out.println(a2);
		System.out.println(b2);
		
		int a1=1;
		int b1=++a1;
		System.out.println(a1);
		System.out.println(b1);
		
		
		
	}

}
